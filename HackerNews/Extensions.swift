extension UIColor {

    class func textColor() -> UIColor {
        return UIColor.icebergColor()
    }
    
    class func backgroundColor() -> UIColor {
        return UIColor.charcoalColor()
    }
        
    class func separatorColor() -> UIColor {
        return UIColor.coolGrayColor()
    }

}
